/**
 * Result of an initial (lazy) chunk operation for large documents.
 *
 * Contains only enough chunks for the first screenful, with metadata
 * to continue chunking the remaining content progressively.
 *
 * This enables fast initial render for documents that would take
 * too long to chunk entirely before display.
 *
 * @example
 * ```ts
 * const result = chunkDocumentInitial(largeDocument);
 *
 * if (result.isComplete) {
 *   // Document was small enough to chunk entirely
 *   renderAllChunks(result.chunks);
 * } else {
 *   // Render initial chunks, continue in background
 *   renderChunks(result.chunks);
 *   scheduleContinueChunking(result);
 * }
 * ```
 */
export interface LazyChunkResult {
  /** Initial chunks (enough for visible viewport area) */
  chunks: DocumentChunk[];
  /** Estimated total chunk count for the full document (for progress indicators) */
  estimatedTotalChunks: number;
  /** Character offset where initial chunking stopped (for continueChunking) */
  chunkedUntil: number;
  /** Total content length in characters */
  totalLength: number;
  /** Whether all content has been chunked (true for small documents) */
  isComplete: boolean;
}

/**
 * Minimum characters to include in initial chunking for visible area.
 *
 * Assumes ~750 chars per chunk, 16 chunks = ~12,000 chars,
 * which provides enough content for typical viewport heights.
 */
const MIN_INITIAL_CHARS = 12000;

/**
 * Chunk only the initial portion of a large document for fast first render.
 *
 * For documents under ~18k chars, chunks everything immediately.
 * For larger documents, finds a good break point and chunks only
 * the initial portion. Use continueChunking() for the rest.
 *
 * Break point selection:
 * 1. Seeks paragraph boundary (\n\n) near MIN_INITIAL_CHARS
 * 2. Falls back to line boundary (\n)
 * 3. Falls back to word boundary (space)
 * 4. Forces break at MIN_INITIAL_CHARS as last resort
 *
 * @param content - The full document content
 * @param options - Chunking configuration options (optional)
 * @returns LazyChunkResult with initial chunks and continuation metadata
 *
 * @example
 * ```ts
 * // Large document - only initial portion chunked
 * const result = chunkDocumentInitial(hugeNovel);
 *
 * // Render initial content immediately
 * setChunks(result.chunks);
 *
 * // Continue chunking in background
 * if (!result.isComplete) {
 *   requestIdleCallback(() => {
 *     const continued = continueChunking(
 *       hugeNovel,
 *       result.chunkedUntil,
 *       result.chunks
 *     );
 *     setChunks(continued.chunks);
 *   });
 * }
 * ```
 *
 * @example
 * ```ts
 * // Small document - everything chunked at once
 * const result = chunkDocumentInitial(shortText);
 * result.isComplete; // true
 * result.chunks.length === result.estimatedTotalChunks; // true
 * ```
 */
export function chunkDocumentInitial(
  content: string,
  options: ChunkingOptions = {}
): LazyChunkResult {
  const opts = { ...DEFAULT_OPTIONS, ...options };

  if (!content || content.length === 0) {
    return {
      chunks: [],
      estimatedTotalChunks: 0,
      chunkedUntil: 0,
      totalLength: 0,
      isComplete: true,
    };
  }

  // For small documents, just chunk everything
  if (content.length <= MIN_INITIAL_CHARS * 1.5) {
    const fullChunks = chunkDocument(content, opts);
    return {
      chunks: fullChunks,
      estimatedTotalChunks: fullChunks.length,
      chunkedUntil: content.length,
      totalLength: content.length,
      isComplete: true,
    };
  }

  // For large documents, only chunk the initial portion
  // Find a good break point near MIN_INITIAL_CHARS
  const breakPoint = findBreakPoint(content, MIN_INITIAL_CHARS);

  // Chunk only the initial portion
  const initialContent = content.slice(0, breakPoint);
  const initialChunks = chunkDocumentInternal(initialContent, opts, 0);

  // Estimate total chunks based on content length ratio
  const estimatedTotalChunks = Math.ceil(
    (content.length / breakPoint) * initialChunks.length
  );

  return {
    chunks: initialChunks,
    estimatedTotalChunks,
    chunkedUntil: breakPoint,
    totalLength: content.length,
    isComplete: false,
  };
}

/**
 * Continue chunking a large document from where the initial chunking stopped.
 *
 * Processes the next batch of content and appends new chunks to the
 * existing array. Offsets are calculated relative to the full document.
 *
 * Call this repeatedly until isComplete is true to fully chunk a large document.
 *
 * @param content - The full document content (not just remaining portion)
 * @param fromOffset - Character offset to continue from (use result.chunkedUntil)
 * @param existingChunks - Already chunked content (offsets preserved)
 * @param options - Chunking configuration options (optional)
 * @param batchSize - Characters to process in this batch (default: 20000)
 * @returns Object with:
 *   - chunks: Updated array with new chunks appended
 *   - newOffset: Offset to use for next continueChunking call
 *   - isComplete: Whether all content has been chunked
 *
 * @example
 * ```ts
 * // Start with initial chunking
 * let result = chunkDocumentInitial(largeDoc);
 * setChunks(result.chunks);
 *
 * // Continue chunking progressively
 * function chunkMore() {
 *   if (result.isComplete) return;
 *
 *   result = continueChunking(
 *     largeDoc,
 *     result.chunkedUntil,
 *     result.chunks
 *   );
 *   setChunks(result.chunks);
 *
 *   // Schedule next batch
 *   if (!result.isComplete) {
 *     requestIdleCallback(chunkMore);
 *   }
 * }
 *
 * chunkMore();
 * ```
 */
export function continueChunking(
  content: string,
  fromOffset: number,
  existingChunks: DocumentChunk[],
  options: ChunkingOptions = {},
  batchSize: number = 20000
): { chunks: DocumentChunk[]; newOffset: number; isComplete: boolean } {
  const opts = { ...DEFAULT_OPTIONS, ...options };

  if (fromOffset >= content.length) {
    return { chunks: existingChunks, newOffset: content.length, isComplete: true };
  }

  // Find next break point
  const remainingContent = content.slice(fromOffset);
  const endOffset = Math.min(remainingContent.length, batchSize);
  const breakPoint = findBreakPoint(remainingContent, endOffset);

  // Chunk this batch
  const batchContent = remainingContent.slice(0, breakPoint);
  const newChunks = chunkDocumentInternal(batchContent, opts, fromOffset);

  // Merge with existing chunks
  const allChunks = [...existingChunks, ...newChunks];

  const newOffset = fromOffset + breakPoint;
  const isComplete = newOffset >= content.length;

  return {
    chunks: allChunks,
    newOffset,
    isComplete,
  };
}

/**
 * Generator function for progressive document chunking.
 *
 * Yields incremental chunk arrays with progress information,
 * allowing for streaming/chunked rendering without callbacks.
 * Each yield contains all chunks processed so far.
 *
 * This is useful when you want:
 * - Async-style iteration without promises
 * - Cancellable chunking (just stop iterating)
 * - Progress updates during chunking
 *
 * @param content - The full document content
 * @param options - Chunking configuration options (optional)
 * @param batchSize - Characters to process per yield (default: 15000)
 * @yields Object with:
 *   - chunks: All chunks processed so far
 *   - progress: Completion ratio (0 to 1)
 *   - isComplete: Whether chunking is finished
 *
 * @example
 * ```ts
 * // Progressive chunking with generator
 * const generator = chunkDocumentGenerator(largeDoc);
 *
 * for (const result of generator) {
 *   setChunks(result.chunks);
 *   setProgress(result.progress * 100);
 *
 *   if (result.isComplete) {
 *     console.log('Chunking complete!');
 *     break;
 *   }
 * }
 * ```
 *
 * @example
 * ```ts
 * // Cancellable chunking
 * const generator = chunkDocumentGenerator(doc);
 * let cancelled = false;
 *
 * for (const result of generator) {
 *   if (cancelled) break; // Stop whenever you want
 *
 *   renderChunks(result.chunks);
 * }
 * ```
 */
export function* chunkDocumentGenerator(
  content: string,
  options: ChunkingOptions = {},
  batchSize: number = 15000
): Generator<{ chunks: DocumentChunk[]; progress: number; isComplete: boolean }> {
  const opts = { ...DEFAULT_OPTIONS, ...options };

  if (!content || content.length === 0) {
    yield { chunks: [], progress: 1, isComplete: true };
    return;
  }

  // Start with initial chunks
  const initial = chunkDocumentInitial(content, opts);
  yield {
    chunks: initial.chunks,
    progress: initial.chunkedUntil / content.length,
    isComplete: initial.isComplete,
  };

  if (initial.isComplete) {
    return;
  }

  // Continue chunking in batches
  let currentChunks = initial.chunks;
  let currentOffset = initial.chunkedUntil;

  while (!initial.isComplete && currentOffset < content.length) {
    const result = continueChunking(
      content,
      currentOffset,
      currentChunks,
      opts,
      batchSize
    );

    currentChunks = result.chunks;
    currentOffset = result.newOffset;

    yield {
      chunks: currentChunks,
      progress: currentOffset / content.length,
      isComplete: result.isComplete,
    };

    if (result.isComplete) {
      break;
    }
  }
}

/**
 * Find an optimal break point near the target position.
 *
 * Searches within a ±200 character window for natural break points
 * in order of preference:
 * 1. Paragraph boundary (\n\n) - ideal for preserving structure
 * 2. Line boundary (\n) - acceptable break point
 * 3. Word boundary (space) - minimal disruption
 * 4. Force break at target - last resort
 *
 * @param content - The full document content
 * @param targetPosition - Target character position for the break
 * @returns Character position after the break point (content length if at end)
 *
 * @example
 * ```ts
 * // Prefer paragraph break near target
 * findBreakPoint('Para 1\n\nPara 2\n\nPara 3', 10);
 * // Returns position after 'Para 1\n\n' (at 8)
 *
 * // Force break if no good boundary exists
 * findBreakPoint('abcdefghij', 5); // Returns 5
 * ```
 */
function findBreakPoint(content: string, targetPosition: number): number {
  if (targetPosition >= content.length) {
    return content.length;
  }

  // Search window around target (±200 chars)
  const searchStart = Math.max(0, targetPosition - 200);
  const searchEnd = Math.min(content.length, targetPosition + 200);
  const searchText = content.slice(searchStart, searchEnd);

  // Priority: double newline > single newline > space
  // We want to find the BEST break point, not just any

  // Look for paragraph break (double newline) - ideal
  const paragraphBreak = searchText.indexOf('\n\n');
  if (paragraphBreak !== -1) {
    const absolutePos = searchStart + paragraphBreak + 2;
    if (absolutePos > targetPosition - 100 && absolutePos < targetPosition + 200) {
      return Math.min(absolutePos, content.length);
    }
  }

  // Look for any paragraph break in the window
  let bestParagraphBreak = -1;
  let searchPos = 0;
  while ((searchPos = searchText.indexOf('\n\n', searchPos)) !== -1) {
    const absolutePos = searchStart + searchPos + 2;
    if (absolutePos <= targetPosition + 200) {
      bestParagraphBreak = absolutePos;
    }
    searchPos += 2;
  }
  if (bestParagraphBreak > targetPosition - 100) {
    return Math.min(bestParagraphBreak, content.length);
  }

  // Look for single newline
  const newlinePos = searchText.lastIndexOf('\n', targetPosition - searchStart + 100);
  if (newlinePos !== -1) {
    const absolutePos = searchStart + newlinePos + 1;
    if (absolutePos > targetPosition - 100) {
      return Math.min(absolutePos, content.length);
    }
  }

  // Fall back to word boundary (space)
  const spacePos = searchText.lastIndexOf(' ', targetPosition - searchStart + 100);
  if (spacePos !== -1) {
    const absolutePos = searchStart + spacePos + 1;
    if (absolutePos > targetPosition - 100) {
      return Math.min(absolutePos, content.length);
    }
  }

  // Last resort: force break at target
  return Math.min(targetPosition, content.length);
}

/**
 * Internal chunking function that processes content starting at a given offset.
 *
 * Similar to chunkDocument() but allows specifying the starting offset
 * for correct offset calculation in progressive chunking scenarios.
 *
 * @param content - Content to chunk (partial document content)
 * @param opts - Full chunking options (required)
 * @param startOffset - Starting offset for offset calculation
 * @returns Array of chunks with correct global offsets
 *
 * @internal Used by chunkDocumentInitial and continueChunking
 */
function chunkDocumentInternal(
  content: string,
  opts: Required<ChunkingOptions>,
  startOffset: number
): DocumentChunk[] {
  const chunks: DocumentChunk[] = [];

  if (!content || content.length === 0) {
    return chunks;
  }

  // Split into logical blocks first
  const blocks = splitIntoBlocks(content);

  let currentOffset = startOffset;
  let lineNumber = 1;

  // Calculate initial line number from start offset if not 0
  if (startOffset > 0) {
    // This is a simplification - for perfect accuracy we'd need the full document
    lineNumber = 1; // Will be corrected when merged with full chunks
  }

  for (const block of blocks) {
    // Split large blocks if needed
    const subChunks = splitLargeBlock(block, opts.maxChunkSize, opts.targetChunkSize);

    for (let i = 0; i < subChunks.length; i++) {
      const subChunk = subChunks[i];
      const type = detectChunkType(subChunk);

      const chunk: DocumentChunk = {
        id: generateChunkId(),
        content: subChunk,
        startOffset: currentOffset + (i > 0 ? subChunks.slice(0, i).join('').length : 0),
        endOffset: currentOffset + subChunks.slice(0, i + 1).join('').length,
        type,
        estimatedHeight: estimateChunkHeight(subChunk, type, opts),
        isPartial: subChunks.length > 1,
        lineNumber,
      };

      chunks.push(chunk);

      // Update line number for next chunk
      lineNumber += subChunk.split('\n').length - 1;
    }

    // Update current offset for next block
    currentOffset += block.length;
    lineNumber += block.split('\n').length - 1;
  }

  // Recalculate offsets to ensure accuracy
  let offset = startOffset;
  for (const chunk of chunks) {
    chunk.startOffset = offset;
    chunk.endOffset = offset + chunk.content.length;
    chunk.id = getStableChunkId(chunk.startOffset);
    offset = chunk.endOffset;
  }

  return chunks;
}

/**
 * Estimate the number of chunks in a document without actually chunking it.
 *
 * Fast O(1) calculation useful for:
 * - Progress bar sizing before chunking starts
 * - Deciding between full and lazy chunking
 * - Memory estimation
 *
 * Note: This is an approximation. Actual chunk count may vary due to
 * block boundaries and content structure.
 *
 * @param content - Document content to estimate
 * @param avgChunkSize - Average chunk size assumption (default: 750)
 * @returns Estimated number of chunks (rounded up)
 *
 * @example
 * ```ts
 * // Estimate for progress indicator
 * const estimated = estimateChunkCount(largeDoc);
 * setTotalChunks(estimated);
 *
 * // Compare with actual
 * const actual = chunkDocument(largeDoc).length;
 * console.log(`Estimated: ${estimated}, Actual: ${actual}`);
 * // Estimated: 134, Actual: 138 (close enough for progress bars)
 * ```
 */
export function estimateChunkCount(content: string, avgChunkSize: number = 750): number {
  if (!content || content.length === 0) {
    return 0;
  }
  return Math.ceil(content.length / avgChunkSize);
}

/**
 * Quickly determine if a document is "large" and needs lazy chunking.
 *
 * Uses character count (O(1)) instead of word count for speed.
 * Default threshold of 30,000 chars ≈ 10,000 words (Chinese) or
 * 5,000 words (English, assuming 6 chars/word average).
 *
 * @param content - Document content to check
 * @param threshold - Character threshold (default: 30000)
 * @returns true if document length exceeds threshold
 *
 * @example
 * ```ts
 * // Decide chunking strategy
 * if (isLargeDocument(content)) {
 *   // Use lazy chunking for fast initial render
 *   const result = chunkDocumentInitial(content);
 *   return result;
 * } else {
 *   // Full chunking is fast enough
 *   return chunkDocument(content);
 * }
 * ```
 *
 * @example
 * ```ts
 * // Custom threshold for specific use case
 * isLargeDocument(content, 50000); // Only lazy chunk very large docs
 * isLargeDocument(content, 10000); // Lazy chunk more aggressively
 * ```
 */
export function isLargeDocument(content: string, threshold: number = 30000): boolean {
  return content.length > threshold;
}
